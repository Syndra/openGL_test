	//proj
	glm::mat4 Projection = glm::perspective(glm::radians(45.0f), (float)4 / (float)3, 0.1f, 100.0f);
	//camera(view)
	glm::mat4 View = glm::lookAt(glm::vec3(4, 3, -3), glm::vec3(0, 0, 0), glm::vec3(0, 1, 0));
	//model
	glm::mat4 Model = glm::mat4(1.0f);
	//mvp
	glm::mat4 mvp = Projection * View * Model;

	//triagle translate matrix
	glm::mat4 Translate = glm::translate(glm::mat4(1), glm::vec3(-0.1f, -0.1f, -0.1f));
	glm::mat4 tri_mvp = mvp * Translate;

	//give to GLSL
	glUseProgram(programID);
